    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        String correo= txtCorreo.getText();
        char[] claveArray = txtClave.getPassword();
        String clave = new String(claveArray);
        
        if (correo.isEmpty() || clave.isEmpty()){
            JOptionPane.showMessageDialog(null, "Llene completamente los campos");
            return;
        }
        
        try {
            String sql = "SELECT * FROM usuarios WHERE correo = ? AND clave = ?";
            PreparedStatement ps = cn.prepareStatement(sql);
            ps.setString(1, correo);
            ps.setString(2, clave);
            
            ResultSet rs = ps.executeQuery();
            if(rs.next() ){
            JOptionPane.showMessageDialog(null, "Bienvenido al sistema");}
            else{
            JOptionPane.showMessageDialog(rootPane, "Error en las credencial");
            return;
            }
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error al conecta la BD" + e.getMessage());
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

